/**
 * 獲取開發者模式的錯誤訊息
 */
function getErrorMessage(error) {
    let errorMsg = `❌ 欸出錯了：${error.message}`;
    if (error.message.includes('404')) errorMsg = `❌ 找不到模型欸，要不要換一個？`;
    if (error.message.includes('429')) errorMsg = '⚠️ 請求太頻繁了，等一下再試吧';
    return errorMsg;
}

