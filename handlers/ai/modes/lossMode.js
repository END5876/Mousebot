/**
 * 獲取損友模式的錯誤訊息
 */
function getErrorMessage(error) {
    let errorMsg = `❌ 錯誤：${error.message}`;
    if (error.message.includes('404')) errorMsg = `❌ 找不到模型，請嘗試更改模型名稱`;
    if (error.message.includes('429')) errorMsg = '⚠️ 請求太頻繁，請稍後再試';
    return errorMsg;
}

